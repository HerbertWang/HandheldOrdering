package com.alex.food.bean;

/**
 * 
 * @author ALEX
 *
 */
public class TableSectionList {
	private String sectionId;
	private String sectionName;
	private String sectionNameAlt;

	public String getSectionId() {
		return sectionId;
	}

	public void setSectionId(String sectionId) {
		this.sectionId = sectionId;
	}

	public String getSectionName() {
		return sectionName;
	}

	public void setSectionName(String sectionName) {
		this.sectionName = sectionName;
	}

	public String getSectionNameAlt() {
		return sectionNameAlt;
	}

	public void setSectionNameAlt(String sectionNameAlt) {
		this.sectionNameAlt = sectionNameAlt;
	}
}
